#!/bin/bash
# Скрипт для запуска всех компонентов системы одновременно

echo "Запуск всех компонентов системы..."

# Запуск веб-интерфейса в фоновом режиме
echo "Запуск веб-интерфейса..."
./run.sh web &
WEB_PID=$!

# Небольшая пауза для старта веб-интерфейса
sleep 2

# Запуск рекордера
echo "Запуск рекордера..."
./run.sh recorder

# Если рекордер завершится, завершаем также веб-интерфейс
kill $WEB_PID 2>/dev/null